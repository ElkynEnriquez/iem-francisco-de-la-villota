<p>FUNCIONES  PARA MANIPULADORAS DE ALIMENTOS 
*Portar adecuadamente el uniforme otorgado por la entidad contratista, al igual que el gorro y el tapabocas. 
* Preparar y servir los alimentos debidamente, previos a consumir. 
* Ofrecer buen trato a los niños usuarios.
 *Crear en los niños hábitos de higiene y normas en la mesa. 
* Mantener los espacios y utensilios en orden y limpios. </p><br>

<p>FUNCIONES DEL CONSEJO DE PADRES DE FAMILIA: 
El consejo de padres de familia deberá conformarse en todos los establecimientos educativos. Podrá organizar los comités de trabajo que guarden afinidad con el proyecto educativo institucional y el plan de mejoramiento del establecimiento educativo, de conformidad con los planes de trabajo que acuerde con el rector.  El consejo de padres es un órgano de participación educativa que no requiere registro ante ninguna autoridad y para pertenecer a él no se podrán establecer cuotas de afiliación o contribución económica de ninguna especie. Se reunirá como mínimo tres veces al año por convocatoria del rector o por derecho  propio. </p><br>


<p>FUNCIONES DEL COORDINADOR ACADEMICO:

El coordinador   Académico (Directivo Docente), depende  del   rector   es responsable de acompañar el proceso educativo de la Institución de acuerdo a su filosofía y Modelo Pedagógico.   Bajo su dependencia están por relación de autoridad funcional, los profesores.  Sus funciones están enunciadas de manera expresa en las leyes 715 de 2001 y 115 de 1994 y los decretos 1860 de 1994, 1278 de 2002, 1850 de 2002 y 3020 de 2002. </p><br>

<p>FUNCIONES DE LOS DOCENTES:
