      <p>En la Institución Educativa Francisco de la Villota existe un Manual para la  Convivencia, diseñado  de conformidad con lo establecido en el artículo 23 del Decreto 1860 de 1.994, y de acuerdo al modelo pedagógico adoptado: ENFOQUE POR COMPETENCIAS, tiene como propósito fundamental establecer los acuerdos de convivencia que regulan el ejercicio de los derechos y el cumplimiento de los deberes de la comunidad educativa de la Institución. Las normas establecidas son el marco de referencia para mantener una sana convivencia de toda la familia Francisco de la Villota, donde reine la armonía, el respeto, la cooperación y la cordialidad en el desarrollo de las distintas actividades cotidianas de la vida escolar. La armonía en la convivencia de todos los estamentos de la Institución debe estar centrada en el respeto de los derechos de los demás, en el estricto cumplimiento de los deberes de cada quien y en la búsqueda permanente de la calidad en los aprendizajes de los estudiantes. Los acuerdos de convivencia contenidos en este Manual   son elaborados con la participación directa de estudiantes, profesores y padres de familia, basada en el estricto cumplimiento de los deberes, en la formalidad y rigor académico y en la creatividad de los educadores para desarrollar  estrategias didácticas que articulen los aprendizajes con la realidad del estudiante y de su entorno.</p>
