   <font color="black">    MANTENIMIENTO DE LA PLANTA FÍSICA
<br>

La institución educativa Francisco de la Villota  cuenta con una planta física que presenta ciertas deficiencias,  se brinda el servicio educativo en las siguientes sedes: Escuela Integrada Genoy, Escuela Charguayaco, Escuela Las Cuadras Villa María; Escuela Pullitopamba y la cede principal Colegio. En la cede principal Colegio y demás cedes se trabaja por la preservación, cuidado y mantenimiento de la planta física con apoyo de los proyectos como  PRAE, Gestión de riesgo escolar y sus respectivos comités de apoyo.
<br>

SEGUMIENTO AL USO DE LOS ESPACIOS
<br>

La Institución Educativa con sus respectivas sedes cuenta con  recursos económicos  para realizar un programa apropiado de mantenimiento administración de espacios, equipos, material didáctico, seguridad y protección para los estudiantes. Se tiene claridad sobre las limitaciones de los recursos, distribución de espacios y condiciones locativas; por ello, durante este año se ha gestionado de manera permanente a través de proyectos financieros ante la Administración Municipal, para obtener recursos que permitan el mejoramiento y dotación de una planta física acorde  a las necesidades de la comunidad educativa y requerimientos académicos para lograr un servicio eficiente y de calidad.
<br>

ADQUISICIÓN  DE LOS RECURSOS PARA EL APRENDIZAJE
<br>

La institución cuenta con un plan de adquisición de los recursos para el aprendizaje que consulta las demandas y necesidades de cada sede manifestadas por docentes y estudiantes, para su respectivo direccionamiento, el cual es aprobado por el Consejo Directivo de la Institución.
<br>

SUMINISTROS Y DOTACION
<br>

La adquisición de los suministros se realiza teniendo en cuenta las necesidades de cada sede al inicio del año escolar, para lo cual se requiere presentar  el formato de proyecto de necesidades diligenciado por la institución, teniendo en cuenta el presupuesto anual del Fondo de Servicios Educativos, contabilidad, ingresos y gastos, control fiscal. <br>


