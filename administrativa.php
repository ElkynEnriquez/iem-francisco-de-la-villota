<p>FUNCIONES DEL RECTOR:
El inmediato superior de los docentes es el Rector  del Establecimiento educativo, lo anterior en virtud a las facultades establecidas en el artículo 25 del decreto 1860 de 1994 el cual establece entre las funciones del rector. </p>


