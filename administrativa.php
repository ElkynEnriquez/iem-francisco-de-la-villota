<br>

SEGURIDAD Y PROTECCIÓN 
<br>

La institución revisa y actualiza periódicamente el panorama de riesgos a través  del proyecto Gestión de Riesgo Escolar. Dispone de diferentes mecanismos y estrategias para prevenir posibles riesgos que podrían afectar el buen funcionamiento de la institución y el bienestar de la comunidad educativa. Puesto que el corregimiento de Genoy está ubicado en una zona de amenaza volcánica. (ZAVA).
La población estudiantil en su gran mayoría cuenta con el carne de seguro.
En la sede Colegio se cuenta con el servicio de vigilancia durante las 24 horas, contratado por la Administración Municipal. En las sedes se cuenta con los conserjes, quienes permanecen en cada sede realizando su trabajo durante 8 horas diarias. Pese a esto en la sede Integrada de Genoy se evidencia la necesidad de mejorar la infraestructura para el cierre de la escuela y así fortalecer la seguridad y protección.
<br>

