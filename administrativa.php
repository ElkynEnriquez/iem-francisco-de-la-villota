 <p>CONSEJO ACADÉMICO:
Es la instancia directiva encargada de orientar la marcha pedagógica de la Institución, en los procesos de orientación académica de la Institución educativa.  Está integrado por: La señora Rectora  quien lo preside y convoca, un docente por área. </p>
