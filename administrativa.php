<p>FUNCIONES DE LA SECRETARIA:
La persona designada para desempeñar este cargo  debe velar por el diligenciamiento y cumplimiento de los libros reglamentarios, registros y controles de tipo académico y de archivo (vigente y “muerto”) tanto a nivel interno como externo, administrar la documentación de todo el personal de la institución y el archivo. </p><br>

<p>FUNCIONES DEL PERSONAL DE SERVICIOS GENERALES:
