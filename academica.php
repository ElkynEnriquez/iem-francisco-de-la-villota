      <h4>Enfoque por Competencias</h4>
      <div class="image"><img src="img/ser.PNG"></div><br>
      <p>Chomsky (1985), a partir de las teorías del lenguaje, instaura el concepto y define competencias como la capacidad y disposición para el desempeño y para la interpretación. La educación basada en competencias (Holland, 1966-97) se centra en las necesidades, estilos de aprendizaje y potencialidades individuales para que el alumno llegue a manejar con  destreza los saberes en un momento dado. Superando las incertidumbres, propendiendo por ir más allá de lo que el común de sus congéneres lo hace. </p><br>
<p>Siendo asi, las competencias  en  Francisco de la Villota   tendrán  convergencia en sus  comportamientos sociales, afectivos, mejorando sus  habilidades cognoscitivas, psicológicas, sensoriales y motoras, las que le  permitirán al estudiante asumir  adecuadamente determinados roles,  desempeños, actividades  o tareas. </p> <br>
<p>Con las cuales el  estudiante de Francisco de la Villota demostrará  saber, saber hacer, saber ser y saber convivir  e interactuar en contexto, para lograrlo es trascendental que  el docente le esté retroalimentando con estrategias generadoras de nuevos pensamientos, nuevas maneras de ver la vida, que lo motiven a aprender más y de mejor manera. Teniendo en cuenta que él ya ha caminado por la vida, tiene  experiencia; en cambio a quien le comparte los saberes (ser cognoscente)  puede estar vacío; por lo tanto se lo deberá llenar con su cooperación, intentando comprenderlo; sin desanimarse  porque de acuerdo  al psicólogo Duarte K., deberá mirar al niño o al joven  como “un sector social que presenta experiencias de vida heterogéneas, con capacidades y potencialidades, como un grupo social que busca resolver una tensión existencial entre las ofertas y los requerimientos del mundo adulto para insertarse en dichos ofrecimientos, aquello que desde sus propios sueños y expectativas decide realizar y una situación socioeconómica que condiciona las posibilidades de tales proyectos.” 4</p><br>

