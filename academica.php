              <h4>EVALUACION POR COMPETENCIAS:</h4>
              <p>El principal referente conceptual del proceso de evaluación de competencias lo proporciona el Decreto Ley 1278 de 2002. Esta norma en su artículo 35 define una competencia como “una característica subyacente en una persona causalmente relacionada con su desempeño y actuación exitosa en un puesto de trabajo, y señala también que la evaluación de competencias debe permitir la valoración de por lo menos los siguientes aspectos:</p>
                <p>• Competencias de logro y acción; </p><p> • competencias de ayuda y servicio; </p><p> • competencias de influencia;  
                • competencias de liderazgo y dirección;  </p><p>• competencias cognitivas y, </p><p> • competencias de eficacia personal"11 
                La evaluación por competencias   valora: </p><p>
                </p><p>• La interacción  permanente de: valores, actitudes, motivaciones, intereses, rasgos de personalidad, entre otras.  </p><p> • Conocimientos previos y nuevos, habilidades  interiorizadas en cada persona, a las cuales puede acudir para abordar y solucionar situaciones concretas.</p><p>

                • El no ser  estática; porque  se construye, asimila y desarrolla con el aprendizaje cotidiano y su práctica permanente, lo que permite  a cada persona  alcanzar  niveles de desempeño cada vez más altos, partiendo de la valoración de su ritmo o estilo de aprendizaje. </p><br><p>


                Estrategias para el trabajo con estudiantes de inclusión: </p><br><p>
                El concepto de inclusión que se quiere continuar  trabajando  en la Institución Educativa Francisco de la Villota del Corregimiento de Genoy,  está  ligado  indisoluble a los conceptos de participación,  equidad y de justicia social. Concibiéndose  a la  inclusión como la “capacidad del Estado y de la Sociedad para generar las condiciones materiales que permitan a los ciudadanos y ciudadanas ser parte de una colectividad y de una organización social que garantiza sus derechos de forma integral. Al mismo tiempo, la inclusión se propone como el ejercicio de una ciudadanía activa en donde los sujetos establecen vínculos vitales consigo mismos y con los demás seres humanos, con el propósito de participar en el desarrollo social, mediante el reconocimiento de la diversidad y de la diferencia. ”12 </p><br><p>
                De ahí que  los diferentes  planes de estudios, serán  permeados por inagotables estrategias que faciliten el avance en diferentes campos de aquellos estudiantes que por manifestar algunas de estas características requieren que el docente sin descuidar al resto de  estudiantes, facilite procesos que favorezcan el avanzar de estos  estudiantes. </p><br><p>
