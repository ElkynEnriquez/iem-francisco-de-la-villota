<br>
<p>La educación basada en competencias  </p>
<p>Sabiendo que las competencias son “los conocimientos, habilidades y destrezas que desarrolla una persona para comprender, transformar y participar en el mundo en el que vive y comprendiendo que la  competencia no es una condición estática, sino que es un elemento dinámico que está en continuo desarrollo por lo que puede generar, potenciar apoyar y promover el conocimiento”3 </p><br>
<p>Siendo así la educación fundamentada en el enfoque por  competencias se transforma en  una nueva orientación educativa que pretende dar respuestas a la sociedad de la información. Facilitando herramientas y estrategias para las nuevas generaciones que ahora tienen en sus contextos diversidad de ayudas técnicas y tecnológicas para su transformación, las cuales bien empleadas pueden ser de utilidad para su avanzar  cognitiva  y actitudinalmente positiva..</p>
